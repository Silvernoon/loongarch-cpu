# =============================================================================
# demo_all.asm
#
# 答辩演示程序：用一个汇编文件覆盖本 LoongArch32 CPU 已实现的全部指令。
# 该文件用于在 LARS/MARS 中打开并单步执行；Vivado CPU 测试使用的
# sim/demo_all.hex 由同一份汇编内容生成。
# 寄存器使用 $N 写法，便于 LARS 直接打开；tools/la32.py 也支持该写法。
# =============================================================================

# ---- 算术、逻辑和立即数指令 ------------------------------------------------
    lu12i.w    $1, 0x7abcd          # $1 = 0x7abcd000
    ori        $1, $1, 0x123        # $1 = 0x7abcd123
    pcaddu12i  $2, 0                # 测试 PC 相对高位立即数加法

    addi.w     $3, $0, 10           # $3 = 10
    addi.w     $4, $0, 20           # $4 = 20
    add.w      $5, $3, $4           # $5 = 30
    sub.w      $6, $4, $3           # $6 = 10
    slt        $7, $3, $4           # 有符号比较：10 < 20
    sltu       $8, $4, $3           # 无符号比较：20 < 10，结果为 0
    slti       $9, $3, 11           # 10 < 11
    sltui      $10, $3, 11          # 无符号比较：10 < 11
    and        $11, $3, $4
    or         $12, $3, $4
    xor        $13, $3, $4
    nor        $14, $3, $0
    andi       $15, $1, 0xfff
    xori       $16, $15, 0x0ff

# ---- 移位指令 --------------------------------------------------------------
    slli.w     $17, $3, 2
    srli.w     $18, $17, 1
    addi.w     $19, $0, -16
    srai.w     $20, $19, 2
    addi.w     $21, $0, 3
    sll.w      $22, $3, $21
    srl.w      $23, $22, $21
    sra.w      $24, $19, $21

# ---- 乘法和除法指令 --------------------------------------------------------
    mul.w      $25, $3, $4
    mulh.w     $26, $19, $4
    mulh.wu    $27, $19, $4
    div.w      $28, $25, $3
    mod.w      $29, $25, $4
    div.wu     $30, $25, $3
    mod.wu     $31, $25, $3

# ---- 访存 load/store 指令 --------------------------------------------------
    lu12i.w    $2, 0
    ori        $2, $2, 0x300        # 数据区基地址 = 0x300
    st.w       $1, $2, 0
    ld.w       $5, $2, 0
    st.h       $19, $2, 4
    ld.h       $6, $2, 4
    ld.hu      $7, $2, 4
    st.b       $19, $2, 8
    ld.b       $8, $2, 8
    ld.bu      $9, $2, 8

# ---- 条件分支指令 ----------------------------------------------------------
    addi.w     $10, $0, 5
    addi.w     $11, $0, 5
    addi.w     $12, $0, 7

    beq        $10, $11, beq_ok
    addi.w     $13, $0, 100         # beq 正常跳转时跳过
beq_ok:
    bne        $10, $12, bne_ok
    addi.w     $13, $0, 101         # bne 正常跳转时跳过
bne_ok:
    blt        $10, $12, blt_ok
    addi.w     $13, $0, 102         # blt 正常跳转时跳过
blt_ok:
    bge        $12, $10, bge_ok
    addi.w     $13, $0, 103         # bge 正常跳转时跳过
bge_ok:
    bltu       $10, $12, bltu_ok
    addi.w     $13, $0, 104         # bltu 正常跳转时跳过
bltu_ok:
    bgeu       $12, $10, bgeu_ok
    addi.w     $13, $0, 105         # bgeu 正常跳转时跳过
bgeu_ok:

# ---- b / bl / jirl ----------------------------------------------------------
    bl         call_target           # 将返回地址写入 $1
after_call:
    b          end_demo

call_target:
    addi.w     $15, $0, 1            # 证明 call_target 已执行
    jirl       $16, $1, 0            # 返回 after_call
    addi.w     $15, $0, 2            # jirl 正常返回时跳过

end_demo:
    nop
    nop
    nop
