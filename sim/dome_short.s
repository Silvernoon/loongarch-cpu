# dome_short.s
#
# 精简答辩演示程序：从 demo_all.s 的 65 条缩减为 34 条代表性指令。
# LARS 使用 CompactDataAtZero，Vivado CPU RESET_VECTOR = 0x00003000。
# =============================================================================

# ---- 立即数、算术、比较和逻辑 ---------------------------------------------
    lu12i.w   $24, 0x7abcd       # r24 = 0x7abcd000
    ori       $24, $24, 0x123    # r24 = 0x7abcd123，连续依赖用于观察前递
    addi.w    $2,  $0, 10        # r2 = 10
    addi.w    $3,  $0, 20        # r3 = 20
    add.w     $4,  $2, $3        # r4 = 30
    sub.w     $5,  $3, $2        # r5 = 10
    slt       $6,  $2, $3        # r6 = 1
    xor       $7,  $2, $3        # r7 = 30

# ---- 移位 -------------------------------------------------------------------
    slli.w    $8,  $2, 2         # r8 = 40
    addi.w    $9,  $0, -16       # r9 = 0xfffffff0
    srai.w    $10, $9, 2         # r10 = 0xfffffffc (-4)

# ---- 乘法和多周期除法 -------------------------------------------------------
    mul.w     $12, $2, $3        # r12 = 200
    div.w     $13, $12, $2       # r13 = 20
    mod.w     $14, $12, $3       # r14 = 0

# ---- 访存、D-Cache 和 load-use 冒险 -----------------------------------------
    addi.w    $15, $0, 0x300     # 数据区基地址
    st.w      $24, $15, 0        # 第一次访问 0x300：D-Cache miss/fill
    ld.w      $16, $15, 0        # 再次访问同一地址：D-Cache hit
    add.w     $17, $16, $0       # 紧跟 ld.w：load-use 停顿
    st.b      $9,  $15, 4        # mem[0x304] 低字节 = 0xf0
    ld.b      $18, $15, 4        # r18 = 0xfffffff0（符号扩展）
    ld.bu     $19, $15, 4        # r19 = 0x000000f0（零扩展）

# ---- 条件分支、循环和 I-Cache 重复取指 -------------------------------------
    beq       $4, $7, beq_ok     # 30 == 30，taken
    addi.w    $20, $0, 100       # 错误路径，不应提交

beq_ok:
    addi.w    $20, $0, 2

loop:
    addi.w    $20, $20, -1
    bne       $20, $0, loop      # 第一次 taken，第二次 not-taken

# ---- BL/JIRL 调用返回和无条件跳转 ------------------------------------------
    bl        call_target         # r1 = after_call 地址

after_call:
    b         end_dome

call_target:
    addi.w    $21, $0, 1         # 证明函数体执行
    jirl      $22, $1, 0          # 返回 after_call
    addi.w    $21, $0, 2         # 错误路径，不应提交

end_dome:
    nop
    nop
    nop
